package day04_variable_intro;

public class FirstVariable {
    public static void main(String[] args) {

        //Declare variable n that can store int(whole numbers)
        int n;
        // assign value 10 to n
        n = 10;
        //print value of an variable
        System.out.println(n);
        System.out.println("n");


        int apple;
        apple = 50;
        System.out.println(apple);


        //declare variable bananas , assign 100, print value out

        int bananas;
        bananas = 100;
        System.out.println(bananas);


    }
}
